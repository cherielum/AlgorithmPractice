//Return the Sum of Two Numbers 
//Directions: Create a method that takes two integers 
//as arguments and return their sum

public class sumOfTwoNumbers {
	public static int sum (int a, int b) {
		//sum is when elements are added together
		//if a is element 1 and b is element 2 
		//then they need to be 
		return a + b;
	}	
	
	public static void main(String[] args) {
		System.out.println(sum(3,2));
		System.out.println(sum(-3,-6));
		System.out.println(sum(7,3));
	}
};
